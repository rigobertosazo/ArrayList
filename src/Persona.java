
public class Persona 
{
    int edad;
    String nombre;
    String apellido;
    String color_piel;
    double altura;
    int telefono;
    
    //creando constructor

    public Persona(int edad, String nombre, String apellido, String color_piel, double altura, int telefono) {
        this.edad = edad;
        this.nombre = nombre;
        this.apellido = apellido;
        this.color_piel = color_piel;
        this.altura = altura;
        this.telefono = telefono;
    }
    
}
